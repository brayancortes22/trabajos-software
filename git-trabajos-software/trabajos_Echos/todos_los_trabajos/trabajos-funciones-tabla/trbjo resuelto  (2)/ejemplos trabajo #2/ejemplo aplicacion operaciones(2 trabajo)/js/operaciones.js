function suma(pnumeroUno,pnumeroDos){ //funcion sin parametros
  
    let numeroUno = pnumeroUno;
    let numeroDos = pnumeroDos;
    let resultado;
    resultado = numeroUno + numeroDos;
    return resultado;
}
